int f(int x, std::vector<int> &vec) {
    if (vec[x] == 0) {
        return 1;
    }
    int s = f(vec[x] - 1, vec);
    return s + 1;
}
int main() {
    std::ios::sync_with_stdio(false);
    std::cin.tie(nullptr);
    int N, c;
    std::cin >> N;
    std::vector<int> vec(N - 1);
    for (int i = 0; i < N - 1; i++) {
        std::cin >> vec[i];
    }
    int max = 0;
    for (int i = 0; i < N - 1; i++) {
        c = f(i, vec);
        if (c > max) {
            max = c;
        }
    }
    std::cout << max << '\n';
    return 0;
}
